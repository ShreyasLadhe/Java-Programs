public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 50;
        int b = 5;

        // arithmetic addition
        System.out.println(a + b);

        //arithmetic subtraction
        System.out.println(a - b);

        //arithmetic multiplication
        System.out.println(a * b);

        // arithmetic division
        System.out.println(a / b);

        int c = 10;
        int d = 4;

        //Modulo Operator - Remainder
        System.out.println(c % d);
    }
}