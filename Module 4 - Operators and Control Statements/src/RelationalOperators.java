public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 20;

        // == operator
        System.out.println(a == b);

        // != operator
        System.out.println(a != b);

        // > operator
        System.out.println(a > b);

        // < operator
        System.out.println(a < b);

        // >= operator
        System.out.println(a >= b);

        // <= operator
        System.out.println(a <= b);
    }
}
